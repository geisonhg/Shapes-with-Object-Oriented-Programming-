package Classes;


/**
 *
 * @author geiso
 */
public class Figure {
    public String name;
    public double area;

    public Figure() {
    }

    public Figure(String name, double area) {
        this.name = name;
        this.area = area;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getArea() {
        return area;
    }

    
    
}
